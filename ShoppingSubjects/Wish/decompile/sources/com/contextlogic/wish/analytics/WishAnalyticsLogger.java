    public static void trackEvent(int i, String str, HashMap<String, String> hashMap) {
        BatchLoggingManager.getInstance().trackEvent(i, str, hashMap);
        if (ExperimentDataCenter.getInstance().shouldEnableFirebaseEventLogging()) {
            WishAnalyticsEvent eventFromId = WishAnalyticsEvent.getEventFromId(i);
            if (eventFromId != null) {
                FirebaseAnalytics.getInstance(WishApplication.getInstance()).logEvent(eventFromId.toString(), extraInfoToBundle(hashMap));
            }
        }
    }

    public static void trackEventOnce(WishAnalyticsEvent wishAnalyticsEvent) {
        StringBuilder sb = new StringBuilder();
        sb.append("EventsIDTracked");
        sb.append(wishAnalyticsEvent.getValue());
        String sb2 = sb.toString();
        if (!PreferenceUtil.getBoolean(sb2)) {
            trackEvent(wishAnalyticsEvent);
            PreferenceUtil.setBoolean(sb2, true);
        }
    }

    public static void trackFirstLoginSessionEvent(WishAnalyticsEvent wishAnalyticsEvent) {
        if (WishApplication.getInstance().isFirstLoginSession()) {
            trackEventOnce(wishAnalyticsEvent);
        }
    }

    private static Bundle extraInfoToBundle(HashMap<String, String> hashMap) {
        Bundle bundle = new Bundle();
        if (hashMap != null) {
            for (Entry entry : hashMap.entrySet()) {
                bundle.putString((String) entry.getKey(), (String) entry.getValue());
            }
        }
        return bundle;
    }
